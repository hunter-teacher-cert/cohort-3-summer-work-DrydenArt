import java.io.*;
import java.util.*;

public class Conditional {

  public static void main(String[] args) {
    int a = 2;

    if (a > 20) {
      System.out.println("A is bigger.");
    } 
    else if (a < 20) {
      System.out.println("A is smaller.");
    }
    else {
      System.out.println("A is equal.");
    }

    
  }
}